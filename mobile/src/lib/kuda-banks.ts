// Normalise a bank name for matching: lowercase, fold "&"->"and", drop
// punctuation, strip corporate suffixes, and unify "microfinance bank"/"mfb"
// (Moniepoint writes "X Microfinance Bank", Kuda writes "X MFB").
function normBankName(s: string): string {
  let x = s
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[.,\-/']/g, ' ');
  x = x.replace(/\s+/g, ' ').trim();
  for (const suf of [' plc', ' limited', ' ltd', ' nigeria', ' ng']) {
    while (x.endsWith(suf)) x = x.slice(0, -suf.length).trim();
  }
  x = x
    .replace(/\bmicrofinance bank\b/g, 'mfb')
    .replace(/\bmicrofinance\b/g, 'mfb')
    .replace(/\bmicro finance bank\b/g, 'mfb')
    .replace(/\bmfb bank\b/g, 'mfb')
    .replace(/\s+/g, ' ')
    .trim();
  return x;
}

// Normalised name -> code, computed once. A normalised key shared by >1 bank is
// marked ambiguous (null) so we never guess between e.g. two same-named wallets.
let _byNorm: Map<string, string | null> | null = null;
function byNorm(): Map<string, string | null> {
  if (_byNorm) return _byNorm;
  const m = new Map<string, string | null>();
  for (const b of KUDA_BANKS) {
    const k = normBankName(b.name);
    m.set(k, m.has(k) ? null : b.code);
  }
  _byNorm = m;
  return m;
}

/** Resolve a stored bank name to its Kuda 6-digit code, or null if it can't be
 *  mapped confidently (caller skips + reports that vendor). */
export function kudaCodeForBankName(name: string | null | undefined): string | null {
  if (!name) return null;
  const trimmed = name.trim();
  if (!trimmed) return null;
  const override = KUDA_CODE_OVERRIDES[trimmed];
  if (override) return override;
  return byNorm().get(normBankName(trimmed)) ?? null;
}
